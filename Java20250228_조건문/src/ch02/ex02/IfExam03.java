public class IfExam03 {

	public static void main(String[] args) {

		int kor = 80;
	
		if(kor>=80) {
			
			System.out.println("상");
			
		
		
		} else {
			if(kor>=60) {
				System.out.println("중");
			}else {
				System.out.println("하");
			}
		}
		
		
		
		
		System.out.println("종료");
		
		
		
	}

}
